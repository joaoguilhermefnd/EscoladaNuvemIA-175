# Define a distância percorrida em quilômetros
distancia_percorrida = 300

# Define a quantidade de combustível gasta em litros
combustivel_gasto = 25

# Calcula o consumo médio (distância percorrida / combustível gasto)
# O resultado será em quilômetros por litro (km/l)
consumo_medio = distancia_percorrida / combustivel_gasto

# Exibe todos os dados da viagem e o consumo médio
print(f"Distância percorrida: {distancia_percorrida} km")
print(f"Combustível gasto: {combustivel_gasto} litros")
print(f"Consumo médio: {consumo_medio:.2f} km/l")