# Define as notas do aluno
nota1 = 7.5
nota2 = 8.0
nota3 = 6.5

# Calcula a média das notas
# A média é a soma de todas as notas dividida pelo número de notas
media = (nota1 + nota2 + nota3) / 3

# Exibe todas as notas e a média final
print(f"Nota 1: {nota1:.2f}")
print(f"Nota 2: {nota2:.2f}")
print(f"Nota 3: {nota3:.2f}")
print(f"Média Final: {media:.2f}")