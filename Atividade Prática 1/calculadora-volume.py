# Define o comprimento da caixa em centímetros
comprimento = 12

# Define a largura da caixa em centímetros
largura = 14

# Define a altura da caixa em centímetros
altura = 20

# Calcula o volume da caixa retangular (comprimento * largura * altura)
volume = comprimento * largura * altura

# Imprime o volume calculado, junto com a unidade (cm³)
print(f"O volume da caixa é: {volume} cm³")