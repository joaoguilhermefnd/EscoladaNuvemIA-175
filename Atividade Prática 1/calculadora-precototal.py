# Define o nome do produto
nome_produto = "Cadeira Infantil"

# Define o preço unitário do produto
preco_unitario = 12.40

# Define a quantidade comprada do produto
quantidade = 3

# Calcula o preço total da compra (preço unitário * quantidade)
preco_total = preco_unitario * quantidade

# Exibe todas as informações da compra
print(f"Produto: {nome_produto}")
print(f"Preço Unitário: R$ {preco_unitario:.2f}")
print(f"Quantidade: {quantidade}")
print(f"Preço Total da Compra: R$ {preco_total:.2f}")